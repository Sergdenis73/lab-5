#include <stdio.h>
#include <limits.h>

#define M 3  // Кількість рядків
#define N 4  // Кількість стовпців

int main() {
    // Задаємо матрицю C
    double C[M][N] = {
        {1.5, -2.3, 4.0, 0.0},
        {-1.7, 3.2, 5.1, -8.4},
        {0.5, 2.7, -3.1, 6.9}
    };
    
    // Масив для зберігання максимальних значень рядків
    double B[M];
    
    // Знаходимо максимальні елементи кожного рядка
    for (int i = 0; i < M; i++) {
        double max = C[i][0];  // Початкове значення - перший елемент рядка
        for (int j = 1; j < N; j++) {
            if (C[i][j] > max) {
                max = C[i][j];
            }
        }
        B[i] = max;  // Записуємо максимальне значення в масив B
    }
    
    // Виводимо матрицю C для наглядності
    printf("Матриця C[%d×%d]:\n", M, N);
    for (int i = 0; i < M; i++) {
        for (int j = 0; j < N; j++) {
            printf("%6.2f ", C[i][j]);
        }
        printf("\n");
    }
    
    // Виводимо масив B
    printf("\nМасив B з максимальних значень кожного рядка:\n");
    for (int i = 0; i < M; i++) {
        printf("Рядок %d: %6.2f\n", i+1, B[i]);
    }
    
    return 0;
}